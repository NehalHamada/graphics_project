c4=Circle((-160,180),20,edgecolor='k',facecolor='k') 
c5=Circle((-160,180),10,edgecolor='k',facecolor='white') 
c6=Circle((-160,180),5,edgecolor='k',facecolor='grey') 
p=np.array([[-30,180],[0,180],[-10,120],[-30,120],[-50,60],[-140,60],[-150,120],[-180,120],[-200,180],[-180,180],[-180,160],[-140,160],[-140,180],[-70,180],[-70,160],[-30,160],[-30,180]]) 
r1=Rectangle((-50,80),30,35,edgecolor='k',facecolor='white') 
r2=Rectangle((-100,80),30,35,edgecolor='k',facecolor='white') 
