import numpy as np 
import matplotlib.pyplot as plt 
from matplotlib.patches import * 
from matplotlib.animation import FuncAnimation
fig=plt.figure()
plt.axis([0,200,200,0]) 
plt.xticks(range(0,200,10)) 
plt.yticks(range(200,0,-10)) 
plt.grid(True) 
axes=plt.gca() 
axes.set_aspect(1) 
plt.text(10,20, 'Nehal Hamada', size=10) 
c1=Circle((-50,180),20,edgecolor='k',facecolor='k') 
c2=Circle((-50,180),10,edgecolor='k',facecolor='white')  
