axes.add_patch(r2) 
axes.add_patch(r3) 
def rotate(i):
    p=[[-30+i,180],[0+i,180],[-10+i,120],[-30+i,120],[-50+i,60],[-140+i,60],[-150+i,120],[-180+i,120],[-200+i,180],[-180+i,180],[-180+i,160],[-140+i,160],[-140+i,180],[-70+i,180],[-70+i,160],[-30+i,160],[-30+i,180]]
    poly.set_xy(p)
    c1.set_center((-50+i,180))
    c2.set_center((-50+i,180))
    c3.set_center((-50+i,180))
    c4.set_center((-160+i,180))
    c5.set_center((-160+i,180))
    c6.set_center((-160+i,180))
    r1.set_xy((-85+i,80))
    r2.set_xy((-135+i,80))
    r3.set_xy((-85+i,120))
    return poly,c1,c2,c3,c4,c5,c6,r1,r2,r3,
anim=FuncAnimation(fig,rotate,frames=400,blit=True,interval=5)
